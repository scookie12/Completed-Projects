
#include <gtest/gtest.h>

#include "Sudoku.hpp"

TEST(SudokuTest, StudentWrittenTests)
{
    Board board = {{{3, 0, 0, 0, 0, 0, 0, 0, 1},
                    {8, 6, 5, 0, 0, 0, 0, 0, 0},
                    {4, 0, 0, 0, 0, 0, 0, 7, 3},
                    {0, 0, 0, 0, 7, 0, 9, 0, 2},
                    {0, 5, 0, 0, 4, 0, 0, 0, 0},
                    {0, 0, 9, 3, 0, 0, 0, 0, 0},
                    {0, 0, 0, 6, 0, 8, 3, 0, 0},
                    {0, 0, 4, 0, 0, 5, 0, 8, 0},
                    {0, 0, 0, 0, 0, 0, 0, 6, 0}} };
    Board response = solve(board);

    Board answer = {{{3, 9, 7, 2, 6, 4, 8, 5, 1},
                     {8, 6, 5, 7, 1, 3, 4, 2, 9},
                     {4, 1, 2, 8, 5, 9, 6, 7, 3},
                     {1, 8, 3, 5, 7, 6, 9, 4, 2},
                     {2, 5, 6, 9, 4, 1, 7, 3, 8},
                     {7, 4, 9, 3, 8, 2, 5, 1, 6},
                     {5, 7, 1, 6, 2, 8, 3, 9, 4},
                     {6, 3, 4, 1, 9, 5, 2, 8, 7},
                     {9, 2, 8, 4, 3, 7, 1, 6, 5}} };
    EXPECT_EQ(response, answer);
}
